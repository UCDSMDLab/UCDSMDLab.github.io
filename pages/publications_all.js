// === Pagination settings ===
const ITEMS_PER_PAGE = 30;
let currentPage = 1;

function renderPublications(page = 1) {
  const start = (page - 1) * ITEMS_PER_PAGE;
  const end = start + ITEMS_PER_PAGE;
  const slicedData = publications.slice(start, end);

  const listContainer = document.getElementById('publication-list');
  listContainer.innerHTML = '<ul id="Publication-list-groups" class="list-group"></ul>';
  const listContainer2 = document.getElementById('Publication-list-groups');

  slicedData.forEach(pub => {
    listContainer2.innerHTML += `
      <li class="list-group-item">
        <strong>${pub.title}</strong><br>
        <em>${pub.journal}</em>, ${pub.details}<br>
        <strong>Authors:</strong> ${pub.authors}<br>
        <a class="float-end btn btn-link" href="${pub.link}" target="_blank">View Publication</a>
      </li>`;
  });

  renderPaginationControls();
}

function renderPaginationControls() {
  const totalPages = Math.ceil(publications.length / ITEMS_PER_PAGE);
  const pagination = document.getElementById('pagination-controls');
  pagination.innerHTML = '';

  for (let i = 1; i <= totalPages; i++) {
    pagination.innerHTML += `
      <li class="page-item ${i === currentPage ? 'active' : ''}">
        <button class="page-link" onclick="goToPage(${i})">${i}</button>
      </li>`;
  }
}

function goToPage(page) {
  currentPage = page;
  renderPublications(currentPage);
}


 runImmediatelyOrOnDOMReady(function () {
  renderPublications(currentPage);
});